public class Puppy {
    
    private String name;

    private String size;

    private String race;
 
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
   
    public String getRace() {
        return race;
    }

    public void setRace(String race) {
        this.race = race;
    }

}
